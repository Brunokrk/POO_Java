package dados;

public class Cidade {
	private String nome;
	private String estado;
	
	public void setNome(String nome) {
		this.nome=nome;
	}
	public String getNome() {
		return this.nome;
	}
	
	public void setEstado() {
		this.estado = estado;
	}
	public String getEstado() {
		return this.estado;
	}
}
