package dados;

public class Extra {
	private int codExtra;
	private int codTipo;
	private int codEstadia;
	private String dia;
	private String hora;
	public int getCodExtra() {
		return codExtra;
	}
	public void setCodExtra(int codExtra) {
		this.codExtra = codExtra;
	}
	public int getCodTipo() {
		return codTipo;
	}
	public void setCodTipo(int codTipo) {
		this.codTipo = codTipo;
	}
	public int getCodEstadia() {
		return codEstadia;
	}
	public void setCodEstadia(int codEstadia) {
		this.codEstadia = codEstadia;
	}
	public String getDia() {
		return dia;
	}
	public void setDia(String dia) {
		this.dia = dia;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	
	
}
