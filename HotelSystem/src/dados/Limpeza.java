package dados;

public class Limpeza {
	private int codLimpeza;
	private String tempo;
	private int codEstadia;
	private int nRegEmp;
	public int getCodLimpeza() {
		return codLimpeza;
	}
	public void setCodLimpeza(int codLimpeza) {
		this.codLimpeza = codLimpeza;
	}
	public String getTempo() {
		return tempo;
	}
	public void setTempo(String tempo) {
		this.tempo = tempo;
	}
	public int getcodEstadia() {
		return codEstadia;
	}
	public void setcodEstadia(int codEstadia) {
		this.codEstadia = codEstadia;
	}
	public int getnRegEmp() {
		return nRegEmp;
	}
	public void setnRegEmp(int nRegEmp) {
		this.nRegEmp = nRegEmp;
	}
	
	
}
